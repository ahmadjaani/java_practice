package demo_package;

public class Demo4 {

	public static void main(String[] args) {
		int i = 50;
		int j = 20;
		int max = (i > j) ? i : j; // ternary operator
		System.out.println("Max : " + max);

		int k = 11;
		++k;
		System.out.println("K : " + k);

	}

}
/*
 * ternary -> 3 operands ;
 * binary -> 2 operands ;
 * unary -> 1 operand;
 */
